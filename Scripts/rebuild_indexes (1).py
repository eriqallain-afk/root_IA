def rebuild_bundles(repo: Path, force: bool) -> List[str]:
    logs: List[str] = []
    knowledge = repo / "90_KNOWLEDGE"
    bundles_dir = knowledge / "BUNDLES"
    bundles_dir.mkdir(parents=True, exist_ok=True)

    # core
    core_path = knowledge / "CONTEXT__CORE.md"
    core_content = _bundle_core_text(repo)
    p, mode = _write_text_safe(core_path, core_content, force)
    logs.append(f"[core] {mode}: {p}")

    # per-team indexes
    for team in ["META", "IAHQ", "OPS"]:
        idx_path = knowledge / f"INDEX__{team}.md"
        idx_content = "\n".join([
            _bundle_team_text(team),
            "",
            "## Références complémentaires",
            "- Équipes : `10_TEAMS/teams.yaml`",
            "- Agents : `00_INDEX/agents.yaml`",
            "- Policies : `50_POLICIES/POLICIES__INDEX.md`",
            "",
        ])
        p, mode = _write_text_safe(idx_path, idx_content, force)
        logs.append(f"[index:{team}] {mode}: {p}")

        bundle_path = bundles_dir / f"BUNDLE__{team}_UPLOAD.md"
        bundle_content = "\n".join([
            f"# BUNDLE — {team} (upload)",
            "",
            f"> {AUTO_MARKER}",
            f"> Generated at: {_now_iso()}",
            "",
            "## CORE",
            "",
            core_content,
            "",
            "## INDEX",
            "",
            idx_content,
            "",
            "## Pointeurs rapides",
            "",
            "- Teams : `10_TEAMS/teams.yaml`",
            "- Agents : `00_INDEX/agents.yaml`",
            "- Routage : `80_MACHINES/hub_routing.yaml`",
            "- Playbooks : `40_RUNBOOKS/playbooks.yaml`",
            "- Policies : `50_POLICIES/POLICIES__INDEX.md`",
            "",
        ])
        p, mode = _write_text_safe(bundle_path, bundle_content, force)
        logs.append(f"[bundle:{team}] {mode}: {p}")

    return logs

def mirror_schemas_into_integration_packages(repo: Path, force: bool) -> List[str]:
    """
    Copies SCHEMAS/*.json into 70_INTEGRATION_PACKAGES/SCHEMAS for distribution.
    """
    src = repo / "SCHEMAS"
    dst = repo / "70_INTEGRATION_PACKAGES" / "SCHEMAS"
    logs: List[str] = []
    if not src.exists():
        return ["[schemas] SKIP: SCHEMAS/ not found"]
    dst.mkdir(parents=True, exist_ok=True)
    for f in sorted(src.glob("*.json")):
        target = dst / f.name
        if target.exists() and not force:
            continue
        target.write_text(f.read_text(encoding="utf-8"), encoding="utf-8")
        logs.append(f"[schemas] copied: {target.relative_to(repo)}")
    return logs

def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--root", help="Chemin vers le dossier root_IA", default=None)
    ap.add_argument("--force", help="Écrase même les fichiers non auto-générés", action="store_true")
    ap.add_argument("--bundles", help="Reconstruit CONTEXT__CORE + INDEX + BUNDLES", action="store_true")
    ap.add_argument("--mirror-schemas", help="Copie SCHEMAS/*.json → 70_INTEGRATION_PACKAGES/SCHEMAS/", action="store_true")
    args = ap.parse_args()

    script_path = Path(__file__)
    repo = Path(args.root).resolve() if args.root else _repo_root_from_script(script_path)

    if not repo.exists():
        print(f"ERROR: repo root not found: {repo}", file=sys.stderr)
        return 2

    logs: List[str] = []
    logs += rebuild_teams_index(repo, force=args.force)
    logs += ensure_agent_yaml(repo, force=args.force)
    logs += rebuild_agents_inventory(repo, force=args.force)
    logs += rebuild_policies_index(repo, force=args.force)

    if args.mirror_schemas:
        logs += mirror_schemas_into_integration_packages(repo, force=args.force)

    if args.bundles:
        logs += rebuild_bundles(repo, force=args.force)

    print("\n".join(logs))
    print("\nDONE.")
    return 0

if __name__ == "__main__":
    raise SystemExit(main())
